# In python we have three types of numbers
# # Integer
# # Float
# # Complex , these are used in advanced mathmatics
import math  # This will import the math module
x = 1  # Integer
x = 1.1  # Float
x = 1 + 2j  # Complex

# Artimetic operations
# Addition
x = 10 + 3  # 13
# Subtraction
x = 10 - 3  # 7
# Multiplication
x = 10 * 3  # 30
# Division
x = 10 / 3  # 3.3333333333333335
# Floor division
x = 10 // 3  # 3
# Modulus
x = 10 % 3  # 1
# Exponent
x = 10 ** 3  # 1000

# We also have augmented assignment operator
x = 10
x += 3  # 13, this is the same as x = x + 3
x -= 3  # 7, this is the same as x = x - 3
x *= 3  # 30, this is the same as x = x * 3
x /= 3  # 10.0, this is the same as x = x / 3
x //= 3  # 3, this is the same as x = x // 3
x %= 3  # 1, this is the same as x = x % 3
x **= 3  # 1000, this is the same as x = x ** 3

# Built-in functions for numbers
# Round
round(2.9)  # 3
# Absolute
abs(-2.9)  # 2.9

# Math module
# If you want to use the math module you have to import it first

# If you want to see all the functions in the math module you can use the following syntax
# math.  # This will show you all the functions in the math module

# If you want to use the ceil function you can use the following syntax
math.ceil(2.9)  # 3
# If you want to use the floor function you can use the following syntax
math.floor(2.9)  # 2

# If you want to use the sqrt function you can use the following syntax
math.sqrt(16)  # 4.0

# If you want to use the factorial function you can use the following syntax
math.factorial(5)  # 120

# If you want to use the pow function you can use the following syntax
math.pow(2, 3)  # 8.0

# If you want to use the pi function you can use the following syntax
math.pi  # 3.141592653589793

# If you want to use the e function you can use the following syntax
math.e  # 2.718281828459045

